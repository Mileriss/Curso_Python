#TUPLAS -são listas criadas com indices que não podem ser alterados ao longo do programa
#Os indices indicados na Tupla não podem ser alterados como em Arrays, são indices fixos 
#TUPLA utiliza a mesma estrutura de Array, porém, com o simbolo de () ao invés de []

import os

#ARRAY - Lista
lista_Array=["Item1","Item2","Item3","Item4"]
print(lista_Array[0])

#TUPLA - Lista
lista_Tupla=("Item1","Item2","Item3","Item4",)
print(lista_Tupla)